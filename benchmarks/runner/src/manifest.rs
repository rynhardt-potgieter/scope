use anyhow::{Context, Result};
use sha2::{Digest, Sha256};
use std::io::Write;
use std::path::Path;
use walkdir::WalkDir;

const MANIFEST_FILENAME: &str = ".fixture-manifest.sha256";

/// Generate a SHA256 manifest for all source files in a fixture directory.
///
/// Writes a `.fixture-manifest.sha256` file listing the hash of every
/// `.ts`, `.cs`, `.tsx`, `.js`, `.json` (excluding node_modules and .scope) source file.
pub fn generate_manifest(fixture_path: &Path) -> Result<()> {
    let entries = collect_source_files(fixture_path)?;

    if entries.is_empty() {
        anyhow::bail!(
            "No source files found in {}. Is this a valid fixture directory?",
            fixture_path.display()
        );
    }

    let manifest_path = fixture_path.join(MANIFEST_FILENAME);
    let mut file = std::fs::File::create(&manifest_path)
        .with_context(|| format!("Failed to create manifest at {}", manifest_path.display()))?;

    writeln!(file, "# Generated by: benchmark manifest --generate")?;
    writeln!(file, "# Do not edit manually")?;

    for (relative_path, hash) in &entries {
        writeln!(file, "{}  {}", hash, relative_path)?;
    }

    eprintln!(
        "Manifest generated: {} files hashed -> {}",
        entries.len(),
        manifest_path.display()
    );

    Ok(())
}

/// Verify all source files in a fixture match their manifest hashes.
///
/// Returns Ok(()) if all hashes match, or an error listing which files changed.
pub fn verify_manifest(fixture_path: &Path) -> Result<()> {
    let manifest_path = fixture_path.join(MANIFEST_FILENAME);

    if !manifest_path.is_file() {
        anyhow::bail!(
            "No manifest found at {}. Run 'benchmark manifest --generate' first.",
            manifest_path.display()
        );
    }

    let manifest_content = std::fs::read_to_string(&manifest_path)
        .with_context(|| format!("Failed to read manifest at {}", manifest_path.display()))?;

    let expected: Vec<(String, String)> = manifest_content
        .lines()
        .filter(|line| !line.starts_with('#') && !line.trim().is_empty())
        .filter_map(|line| {
            let mut parts = line.splitn(2, "  ");
            let hash = parts.next()?.to_string();
            let path = parts.next()?.to_string();
            Some((path, hash))
        })
        .collect();

    if expected.is_empty() {
        anyhow::bail!(
            "Manifest at {} is empty or malformed.",
            manifest_path.display()
        );
    }

    let mut failures: Vec<String> = Vec::new();

    for (relative_path, expected_hash) in &expected {
        let full_path = fixture_path.join(relative_path);
        if !full_path.is_file() {
            failures.push(format!("  MISSING: {}", relative_path));
            continue;
        }

        let actual_hash = hash_file(&full_path)?;
        if actual_hash != *expected_hash {
            failures.push(format!("  MODIFIED: {}", relative_path));
        }
    }

    // Check for new files not in manifest
    let current_files = collect_source_files(fixture_path)?;
    let expected_paths: std::collections::HashSet<&str> =
        expected.iter().map(|(p, _)| p.as_str()).collect();

    for (relative_path, _) in &current_files {
        if !expected_paths.contains(relative_path.as_str()) {
            failures.push(format!("  NEW: {}", relative_path));
        }
    }

    if failures.is_empty() {
        eprintln!(
            "Manifest OK: {} files verified in {}",
            expected.len(),
            fixture_path.display()
        );
        Ok(())
    } else {
        anyhow::bail!(
            "Fixture integrity check failed for {}:\n{}\nRun 'benchmark manifest --generate' after intentional fixture changes.",
            fixture_path.display(),
            failures.join("\n")
        );
    }
}

/// Collect all source files and their SHA256 hashes, sorted by path.
fn collect_source_files(fixture_path: &Path) -> Result<Vec<(String, String)>> {
    let source_extensions = ["ts", "tsx", "js", "cs", "json", "csproj", "sln"];

    let mut entries: Vec<(String, String)> = Vec::new();

    for entry in WalkDir::new(fixture_path)
        .into_iter()
        .filter_map(|e| match e {
            Ok(entry) => Some(entry),
            Err(err) => {
                eprintln!("  Warning: skipping entry during manifest scan: {}", err);
                None
            }
        })
        .filter(|e| e.file_type().is_file())
    {
        let path = entry.path();

        // Skip non-source files
        let ext = match path.extension().and_then(|e| e.to_str()) {
            Some(ext) => ext,
            None => continue,
        };
        if !source_extensions.contains(&ext) {
            continue;
        }

        // Skip excluded directories
        let relative = path
            .strip_prefix(fixture_path)
            .context("Failed to compute relative path")?;
        let relative_str = relative.to_string_lossy().replace('\\', "/");

        if relative_str.starts_with("node_modules/")
            || relative_str.starts_with(".scope/")
            || relative_str.starts_with("bin/")
            || relative_str.starts_with("obj/")
            || relative_str == MANIFEST_FILENAME
        {
            continue;
        }

        let hash = hash_file(path)?;
        entries.push((relative_str, hash));
    }

    entries.sort_by(|a, b| a.0.cmp(&b.0));
    Ok(entries)
}

/// Compute the SHA256 hash of a file, returned as a hex string.
fn hash_file(path: &Path) -> Result<String> {
    let content = std::fs::read(path)
        .with_context(|| format!("Failed to read file for hashing: {}", path.display()))?;
    let mut hasher = Sha256::new();
    hasher.update(&content);
    let result = hasher.finalize();
    Ok(format!("{:x}", result))
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::fs;

    #[test]
    fn test_hash_file_deterministic() {
        let dir = tempfile::tempdir().unwrap();
        let file_path = dir.path().join("test.txt");
        fs::write(&file_path, "hello world").unwrap();

        let hash1 = hash_file(&file_path).unwrap();
        let hash2 = hash_file(&file_path).unwrap();
        assert_eq!(hash1, hash2);
        assert_eq!(hash1.len(), 64); // SHA256 hex is 64 chars
    }

    #[test]
    fn test_hash_file_different_content() {
        let dir = tempfile::tempdir().unwrap();
        let file1 = dir.path().join("a.txt");
        let file2 = dir.path().join("b.txt");
        fs::write(&file1, "hello").unwrap();
        fs::write(&file2, "world").unwrap();

        let hash1 = hash_file(&file1).unwrap();
        let hash2 = hash_file(&file2).unwrap();
        assert_ne!(hash1, hash2);
    }

    #[test]
    fn test_generate_and_verify_manifest() {
        let dir = tempfile::tempdir().unwrap();

        // Create some source files
        let src_dir = dir.path().join("src");
        fs::create_dir_all(&src_dir).unwrap();
        fs::write(src_dir.join("main.ts"), "console.log('hello');").unwrap();
        fs::write(src_dir.join("utils.ts"), "export function foo() {}").unwrap();

        // Generate manifest
        generate_manifest(dir.path()).unwrap();

        // Verify should pass
        verify_manifest(dir.path()).unwrap();

        // Modify a file
        fs::write(src_dir.join("main.ts"), "console.log('modified');").unwrap();

        // Verify should fail
        let result = verify_manifest(dir.path());
        assert!(result.is_err());
        let err = result.unwrap_err().to_string();
        assert!(
            err.contains("MODIFIED"),
            "Error should mention MODIFIED: {}",
            err
        );
    }
}
